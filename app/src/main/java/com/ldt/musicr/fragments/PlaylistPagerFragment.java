    public List<Song> getPlaylistWithListId(Playlist list) {

        if(list.name.equals(getString(R.string.playlist_last_added))) return LastAddedLoader.getLastAddedSongs(getContext());
        else if(list.name.equals(getString(R.string.playlist_recently_played))) {
            TopTracksLoader recentloader = new TopTracksLoader(getContext(),TopTracksLoader.QueryType.RecentSongs);
            return SongLoader.getSongsForCursor(TopTracksLoader.getCursor());
        } else if(list.name.equals(getString(R.string.playlist_top_tracks))) {
            TopTracksLoader topTracksLoader = new TopTracksLoader(getContext(),TopTracksLoader.QueryType.TopTracks);
            return SongLoader.getSongsForCursor(TopTracksLoader.getCursor());
        } else
        return PlaylistSongLoader.getSongsInPlaylist(getContext(), list.id);
    }

    private void refreshData() {
        mTitle.setText(mPlaylist.name);

        mAdapter.setData(getPlaylistWithListId(mPlaylist));

        if(mLoadArtwork!=null) mLoadArtwork.cancel();
        mLoadArtwork= new loadArtwork(this);
        mLoadArtwork.execute();
    }

